.MODEL SMALL
.STACK 100H
.DATA
STR1 DB "hello","$" 
STR2 DB "hello","$" 
MSG1 DB "EQUAL$" 
MSG2 DB "NOT EQUAL$"
.CODE 
MAIN PROC
MOV AX,@DATA 
MOV DS,AX
MOV ES,AX 
CLD
LEA SI, STR1 
LEA DI ,STR2 
MOV CX,5 
REPE CMPSB 
JE A
JNE B

A:
MOV DX, OFFSET MSG1 
MOV AH,09H
INT 21H 
HLT
JMP EXIT

B:
MOV DX,OFFSET MSG2 
MOV AH,09H
INT 21H 
HLT
JMP EXIT

EXIT:
ENDP
END MAIN 