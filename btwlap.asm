MODEL SMALL
.STACK 100H
.DATA
N1 DB 10H, 20H, 30H,40H, 50H, 60H, 70H, 80H, 90H, 82H
N2 DB 11H, 22H, 33H, 44H, 55H, 66H, 77H, 88H, 99H, 81H
.CODE
MAIN PROC
MOV AX, @DATA
MOV DS, AX
LEA SI,N1
LEA DI, N2
MOV CX, 000AH
AGAIN: MOV BL, [SI]
MOV AL, [DI]
MOV [SI], AL
MOV [DI],BL
INC SI
INC DI
LOOP AGAIN
MOV AH, 4CH
INT 21H
MAIN ENDP
END MAIN