MODEL SMALL
.STACK 100H
.DATA
N1 DB 10H, 20H, 30H, 40H, 50H, 60H, 70H, 80H, 90H, 99H
N2 DB 10 DUP(0)
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
LEA SI, N1
LEA DI, N2
ADD SI, 09H
ADD DI, 04H
MOV CX, 000AH
AGAIN:MOV BL, [SI]
MOV [DI], BL
DEC SI
DEC DI
LOOP AGAIN
MOV AH, 4CH
INT 21H
MAIN ENDP
END MAIN