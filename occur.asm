.MODEL SMALL
.STACK 100H
.DATA
FOUND DB 'CHARACTER FOUND $' 
NOTFOUND DB 'CHARACTER NOT FOUND $' 
TEXT DB 'HELLO WORLD'
SEARCH DB 'A' 
COUNT DW 13
.CODE 
BEGIN:
MOV AX,@DATA 
MOV DS,AX 
MOV ES,AX 
MOV CX,COUNT
MOV DI,OFFSET TEXT 
MOV AL,SEARCH 
REPNE SCASB
JZ YES
MOV DX,OFFSET NOTFOUND 
MOV AH,09H
INT 21H 
JMP OVER

YES:
MOV DX,OFFSET FOUND 
MOV AH,09
INT 21H

OVER:
MOV AH,4CH 
INT 21h 
END BEGIN
